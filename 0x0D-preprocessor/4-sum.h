#ifndef SUM_MACRO_H
#define SUM_MACRO_H

/**
 * SUM - Computes the sum of two numbers x and y
 * @x: The first number
 * @y: The second number
 *
 * Description: This macro computes the sum of the numbers x and y.
 *
 * Return: The sum of x and y.
 */
#define SUM(x, y) ((x) + (y))

#endif /* SUM_MACRO_H */
